#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != (char)c)
	{
		if (s[i++] == '\0')
			return (NULL);
	}
	return ((char *)&s[i]);
}
