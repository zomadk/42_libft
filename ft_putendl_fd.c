#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int pos;

	pos = 0;
	while (s[pos] != '\0')
	{
		write(fd, &s[pos], 1);
		pos++;
	}
	write(fd, "\n", 1);
}
